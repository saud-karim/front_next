export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState('ar');

  useEffect(() => {
    // Client-side only
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('language') || 'ar';
      setLanguage(savedLanguage);
      document.documentElement.dir = savedLanguage === 'ar' ? 'rtl' : 'ltr';
      document.documentElement.lang = savedLanguage;
    }
  }, []);

  const changeLanguage = (lang: string) => {
    console.log('🔄 Language changing from', language, 'to', lang);
    setLanguage(lang);
    // Client-side only
    if (typeof window !== 'undefined') {
      localStorage.setItem('language', lang);
      document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
      document.documentElement.lang = lang;
      console.log('✅ Language changed to', lang, '- localStorage:', localStorage.getItem('language'));
    }
  };

  const t = (key: string): string => {
    const translation = translations[language as keyof typeof translations];
    return translation[key as keyof typeof translation] || key;
  };

  const getLocalizedText = (obj: any, field: string): string => {
    if (!obj) return '';
    
    // Try to get localized version first
    const localizedField = `${field}_${language}`;
    if (obj[localizedField]) {
      return obj[localizedField];
    }
    
    // Fallback to base field
    if (obj[field]) {
      return obj[field];
    }
    
    // If field is 'name', try common name variants
    if (field === 'name') {
      return obj.name || obj.title || '';
    }
    
    return '';
  };

  const isRTL = language === 'ar';

  return (
    <LanguageContext.Provider value={{ language, setLanguage: changeLanguage, t, isRTL, getLocalizedText }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}; 
